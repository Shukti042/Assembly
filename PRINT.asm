.MODEL SMALL
.STACK 100H
.DATA 
RESULT DB ?,?,?,?
FINAL DB ?,?,?,?
.CODE  
DISPLAY PROC
   MOV SI,0
   WHILE_LOOP:
   MOV DX,0 
   MOV BX,10
   DIV BX  
   ADD DX,'0'
   MOV RESULT+SI,DL 
   ADD SI,1 
   CMP AX,0
   JNZ WHILE_LOOP
   END_LOOP:
   MOV RESULT+SI,'$'
   MOV FINAL+SI,'$'
   MOV CX,SI
   MOV DI,SI
   SUB DI,1
   MOV SI,0 
   REVERSE:
   MOV BL,RESULT+DI
   MOV FINAL+SI,BL
   SUB DI,1
   ADD SI,1
   LOOP REVERSE
   MOV AH,2
   MOV AH,9
   LEA DX,FINAL
   INT 21H 
   ret
DISPLAY ENDP
MAIN PROC 
   MOV AX,@DATA
   MOV DS,AX
   MOV AX,50   
   CALL DISPLAY
   ;RETURN TO DOS
   MOV AH,4CH
   INT 21H
MAIN ENDP    
    END MAIN