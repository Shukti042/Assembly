.MODEL SMALL
.STACK 100H
.DATA 
RESULT DW ?,?,?
FINAL DW ?,?,?
.CODE
MAIN PROC 
   MOV AX,@DATA
   MOV DS,AX
   MOV AH,1
   INT 21H 
   MOV AH,0
   MOV BX,AX
   SUB BX,'0'
   MOV AH,1
   INT 21H  
   MOV AH,0
   SUB AX,'0'
   ADD AX,BX 
   MOV SI,0
   WHILE_LOOP:
   MOV DX,0 
   MOV BX,10
   DIV BX  
   ADD DX,'0'
   MOV RESULT+SI,DX 
   ADD SI,2 
   CMP AX,0
   JNZ WHILE_LOOP
   END_LOOP:
   MOV RESULT+SI,'$'
   MOV FINAL+SI,'$'
   MOV CX,SI
   MOV DI,SI
   SUB DI,2
   MOV SI,0 
   REVERSE:
   MOV BX,RESULT+DI
   MOV FINAL+SI,BX
   SUB CX,1
   SUB DI,2
   ADD SI,2
   LOOP REVERSE
   MOV AH,2
   MOV DL,0DH
   INT 21H
   MOV DL,0AH
   INT 21H
   MOV AH,9
   LEA DX,FINAL
   INT 21H
   ;RETURN TO DOS
   MOV AH,4CH
   INT 21H
MAIN ENDP    
    END MAIN