.MODEL SMALL
.STACK 100H
.DATA 
VAR1 DB ? 
.CODE
MAIN PROC
   MOV AH,1
   INT 21H ;TAKES INPUT IN AL AS ASCII VALUE 
   SUB AL,'0'
   MOV VAR1,AL
   INT 21H 
   SUB AL,'0'
   MOV VAR1+1,AL
   MOV AH,2 
   MOV DL,VAR1+1
   ADD DL,'0'
   INT 21H
   ;RETURN TO DOS
   MOV AH,4CH
   INT 21H
MAIN ENDP    
    END MAIN
    
    